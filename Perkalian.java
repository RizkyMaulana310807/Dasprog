public class Perkalian{
    public static void main(String[] args){
        int a = 100;
        int b = 5;
        int c, d, e, f, g;
        c = a * b;
        d = a + b;
        e = a % b;
        f = a - b;
        g = a / b;
        System.out.println("Hasil perkalian "+c);
        System.out.println("Hasil pertambahan "+d);
        System.out.println("Hasil modulus "+e);
        System.out.println("Hasil pengurangan "+f);
        System.out.println("Hasil bagi " + g);
    }
}
